package view;

public class Ex07 {
	public static void main(String[] args) {
		int a = 5; // primitive
		Integer b = 10; // assign
		Integer c = 11; // assign
		
		// assign object to primitive
		a = b;
		a = c; // check null
		
		// assign primitive to object
		b = a;
		c = a; 
		
		// Casting
		// Boxing : primitive to object
		// Unboxing: object to primitive
		
		// FROM JAVA 1.5
		// AutoBoxing
		// AutoUnBoxing
	}
}

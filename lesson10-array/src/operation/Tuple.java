package operation;

public class Tuple {
private int max;
private int min;
public Tuple(int max, int min) {
	super();
	this.max = max;
	this.min = min;
}
public Tuple() {
	
}
public int getMax() {
	return max;
}
public void setMax(int max) {
	this.max = max;
}
public int getMin() {
	return min;
}
public void setMin(int min) {
	this.min = min;
}

}

package enumeration;

public class Circle {
	//Sửa tên F2
	private Point p;
	private double R;
	
	public Circle(Point p, double R) {
		this.p = p;
		this.R = R;
	}

	public Point getP() {
		return p;
	}

	public void setP(Point p) {
		this.p = p;
	}

	public double getR() {
		return R;
	}

	public void setR(double r) {
		R = r;
	}

	
	
}

package data;

public class Ex01 {
	
	public static void main(String[] args) {
		
		// Số Nguyên
		int a = 10;
		long b = 20;
		
		// Số thực
		float c = 5.5f;
		double d = 5.3;
		
		// Boolean
		boolean e = true;
		
		// Character
		char f = 'r';
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		
		
	}
}
